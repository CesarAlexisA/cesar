    private void btninsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertarActionPerformed
        // TODO add your handling code here:
         
        
        if (estadoG==1){
         Nombres nombres = new Nombres(txtnombres.getText(),txtap.getText(),txtam.getText());  
         Direccion direccion = new Direccion(txtcalle.getText(),txtcp.getText(),txtmunicipio.getText(),txtcolonia.getText(),txtentidad.getText());
         Datos datos = new Datos(nombres,txtfecha.getText(),txtcorreo.getText(),txtrfc.getText(),txtsexo.getText(),direccion); 
        if(lista==null){
            lista = new ArrayList<Datos>();
        }
         lista.add(datos);
        Archivos archivos = new Archivos();
         
        if(archivos.Grabar(lista))
            JOptionPane.showMessageDialog(null,"Se grabo con exito","Informacion",JOptionPane.INFORMATION_MESSAGE);
        else 
            JOptionPane.showMessageDialog(null,"Error al grabar","Informacion",JOptionPane.ERROR_MESSAGE);
        btnnuevo.setEnabled(true);
        btninsertar.setEnabled(false);
        btneditar.setEnabled(true);
        btneliminar.setEnabled(true);
        
        lista= archivos.Leer();
        CargarDatos();
        estadotxt(false);
        }
        if(estadoG == 2){
           Nombres nombres = new Nombres(txtnombres.getText(),txtap.getText(),txtam.getText());  
           Direccion direccion = new Direccion(txtcalle.getText(),txtcp.getText(),txtmunicipio.getText(),txtcolonia.getText(),txtentidad.getText());
           Datos datos = new Datos(nombres,txtfecha.getText(),txtcorreo.getText(),txtrfc.getText(),txtsexo.getText(),direccion); 
           lista.set(fila,datos);
           Archivos archivos = new Archivos();
        if(archivos.Grabar(lista))
            JOptionPane.showMessageDialog(null,"Se actualizo con exito","Informacion",JOptionPane.INFORMATION_MESSAGE);
        else 
            JOptionPane.showMessageDialog(null,"Error al actualizar","Informacion",JOptionPane.ERROR_MESSAGE);
        
            lista= archivos.Leer();
            CargarDatos();
            estadotxt(false);
           }  
        btnnuevo.setEnabled(true);
        btneditar.setEnabled(true);
        btneliminar.setEnabled(true);
            
            
    }//GEN-LAST:event_btninsertarActionPerformed

