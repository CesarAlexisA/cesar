      private void CargarDatos() {
        DefaultTableModel modelotabla = new DefaultTableModel();
        modelotabla.addColumn("Nombres");
        modelotabla.addColumn("Ap");
        modelotabla.addColumn("Am");
        modelotabla.addColumn("Fecha");
        modelotabla.addColumn("Correo");
        modelotabla.addColumn("RFC");
        modelotabla.addColumn("Sexo");
        modelotabla.addColumn("Calle");
        modelotabla.addColumn("C.P.");
        modelotabla.addColumn("Municipio");
        modelotabla.addColumn("Colonia");
        modelotabla.addColumn("Entidad");
        
        tablaDatos.setModel(modelotabla);
        DefaultTableModel tabla = (DefaultTableModel) this.tablaDatos.getModel();
        if (lista != null) {
            for (Datos datos : lista) {
                Object fila[] = new Object[]{datos.getNombres().getNombres(),datos.getNombres().getAp(),datos.getNombres().getAm(), datos.getFecha(), datos.getCorreo(), datos.getRfc(), datos.getSexo(), datos.getDireccion().getCalle(), datos.getDireccion().getColonia(), datos.getDireccion().getMunicipio(), datos.getDireccion().getCp(), datos.getDireccion().getEntidad()};
                tabla.addRow(fila);
            }
        }
    }

